//Important node commands:-
// npm init :- This command initializes a new Node.js project and creates a package.json file.
// npm i -y :- This command installs all the dependencies listed in the package.json file without prompting for confirmation.
// npm i express :- This command installs the Express.js framework, which is a web application framework  
// npm i nodemon :- This command installs Nodemon, a tool that automatically restarts the Node.js application when file changes are detected.

// **
// npm i <package_name> --save-dev :- This command installs a package as a development dependency.
// npm i <package_name> --save :- This command installs a package as a regular dependency.
// npm i -D : // This command is a shorthand for --save-dev, installing a package as a development dependency.

// npm uninstall <package_name> :- This command uninstalls a package from the project.